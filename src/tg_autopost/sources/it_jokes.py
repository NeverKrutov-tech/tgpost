class ItJokesSource(JokeSource):
    name = "it_jokes"

    def fetch(self, limit: int) -> Iterable[Joke]:
        random.shuffle(_IT_JOKES)
        for i, text in enumerate(_IT_JOKES):
            if i >= limit:
                break
            h = hashlib.sha256(text.encode()).hexdigest()[:16]
            yield Joke(
                text=text,
                source_name=self.name,
                source_url="",
                external_id=f"it_joke_{i}",
                content_hash=h,
            )
